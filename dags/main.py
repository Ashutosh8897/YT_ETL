from airflow import DAG
import pendulum
from datetime import datetime, timedelta
from api.video_stats import get_playlistid, get_videoID, extract_video_data, save_to_json

from datawarehouse.dwh import staging_table, core_table
from dataquality.soda import yt_elt_data_qualtiy

#define the Local time
local_tz = pendulum.timezone("Asia/Kolkata")


#Default args
default_args = {
    "owner" : "Ashutosh",
    "depands_on_past" : False,
    "email_on_failure" : False,
    "email_on_retry" : False,
    "email" : "ashutoshd813@gmail.com",
    # "retries" : 1,
    # "retry_delay" : timedelta(minutes = 5),
    "max_active_run" : 1,
    "dagrun_timeout" : timedelta(hours=1),
    "start_date" : datetime(2025,1,1, tzinfo = local_tz)
    # "end_date" : datetime(2030,12,31, tzinfo= local_tz)
}

#Variable
staging_schema = "staging"
core_schema = "core"


with DAG(
    dag_id = "produce_json",
    default_args = default_args,
    description = "DAG to produce JSON file with raw data.",
    schedule = '0 14 * * *',
    catchup = False
) as dag:
    

    #define tasks
    playlist_id = get_playlistid()
    video_ids = get_videoID(playlist_id)
    extract_data = extract_video_data(video_ids)
    save_to_json_task = save_to_json(extract_data)


    #Define dependencies
    playlist_id >> video_ids >> extract_data >> save_to_json_task


# DAG 2: update_db
with DAG(
    dag_id="update_db",
    default_args=default_args,
    description="DAG to process JSON file and insert data into both staging and core schemas",
    catchup=False,
    schedule=None,
) as dag_update:

    # Define tasks
    update_staging = staging_table()
    update_core = core_table()

    # Define dependencies
    update_staging >> update_core


with DAG(
    dag_id="data_quality",
    default_args=default_args,
    description="DAG to check data quality on both layers in db",
    catchup=False,
    schedule='0 15 * * *',
) as dag_update:

    # Define tasks
    soda_validate_staging = yt_elt_data_qualtiy(staging_schema)
    soda_validate_core = yt_elt_data_qualtiy(core_schema)

    # Define dependencies
    soda_validate_staging >> soda_validate_core

 
